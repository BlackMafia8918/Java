public class Demo_Array {
    public static void main(String[] args) {
        int marks[];
        marks = new int[3];
        marks[0] = 10;
        marks[1] = 40;
        marks[2] = 65;

        System.out.println("Marks of 1st Student: " + marks[0] );
        System.out.println("Marks of 2nd Student: " + marks[1] );
        System.out.println("Marks of 3rd Student: " + marks[2] );
    }    
}
